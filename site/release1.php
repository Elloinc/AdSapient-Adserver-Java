<b>May 11, 2005</b> -  AdSapient Inc., provider of cutting-edge ad management software solutions, today announced that it has released a new version of its innovative Ad Network banner ad server.<br><br>
Ad Network is a multi-user system with unlimited number of publishers, advertisers and users with combined roles. Designed to manage over 250M ad views per month from one machine, it perfectly meets the needs of online advertising agencies and large networks of sites, but can be also used by small and medium businesses and individual entrepreneurs who have access to online publishing and advertising sales channels.
<br><br>
Ad Network version 1.4 is distinguished by high performance, convenient modular structure and intelligent algorithms developed by AdSapient Inc. and has the complete functionality of a full-featured banner ad managing solution, including such features as campaign scheduling options, content/GEO/time/traffic targeting, rich media support, advanced reporting options, customization of banner sizes and multiple language support.
<br><br>
One of the major improvements in the new version of Ad Network is simultaneous multisession browsing, which allows the system administrator to follow several sessions from his computer and modify user account settings.
<br><br>
The unique functionality of Ad Network also includes:
<br><br>
    * Smooth cooperation between users;<br>
    * Profit-oriented inner system design leaves all users satisfied with the advertising results;<br>
    * Anti-fraud monitoring and prevention, auto notification in suspicious cases;<br>
    * Possible automatic adjustment of the campaign delivery for better advertising results;<br>
    * Financial reports generated for advertisers and publishers.<br>
